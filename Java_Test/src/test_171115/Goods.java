package test_171115;

public class Goods {
	
	//필드
	private String name;
	private long price;
	
	//생성자
	public Goods(String name, long price) {
		this.name = name;
		this.price = price;
	}

	
	//메소드
	
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public long getPrice() {
		return price;
	}

	public void setPrice(long price) {
		this.price = price;
	}
	
	
	
	
	
	
	
}
